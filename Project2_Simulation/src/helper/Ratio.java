package helper;

public interface Ratio
{
    /**
     * Ratios between radian and degree.
     */
    double RAD_TO_DEG = 180.0 / Math.PI;
    double DEG_TO_RAD = 1.0 / RAD_TO_DEG;

    /**
     * Ratios between meter and centimeter.
     */
    double M_TO_CM = 100.0;
    double CM_TO_M = 1.0 / M_TO_CM;
}
